#!/usr/bin/env python3

# ----------------------------------
# -- Ejemplo de cadena formateada
# ----------------------------------

# -- Definimos las variables
v1 = 1
v2 = 1
suma = v1 + v2
resta = v1 - v2

# -- Cadena formateada
cad = f"La suma de {v1} + {v2} es {suma}"

# -- Imprimimos la cadena
print(cad)

# -- La cadena formateada se puede poner dentro de
# -- la instruccion de print
print(f"La resta de {v1} menos {v2} es {resta}")
