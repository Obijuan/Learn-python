#!/usr/bin/env python3

cad1 = "Cadena1"
cad2 = "Cadena2"
num = 5

# -- Imprimir diferentes objetos usando el separador
# -- por defecto (un espacio)
print("Probando", cad1, num, cad2)

# -- Imprimir los mismos objetos pero usando como
# -- separador una coma (,)
print("Probando", cad1, num, cad2, sep=",")
